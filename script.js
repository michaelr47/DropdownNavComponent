const getArrowUpImg = () => {
    let img1 = document.querySelectorAll('.arrowDown');
    for (const images of img1) {
        images.src = './images/icon-arrow-up.svg';
    }
}

const getArrowDownImg = () => {
    let img1 = document.querySelectorAll('.arrowDown');
    for (const images of img1) {
        images.src = './images/icon-arrow-down.svg';
    }
}